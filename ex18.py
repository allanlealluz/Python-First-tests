import math, trigonometry
catop = float(input('angulo '))
seno = math.sin(math.radians(catop))
print('o seno é {:.2f}'.format(seno))
cosseno = math.cos(math.radians(catop))
print('o cosseno é {:.2f}'.format(cosseno))
tangente = math.tan(math.radians(catop))
print('tangente é {:.2f}'.format(tangente))

